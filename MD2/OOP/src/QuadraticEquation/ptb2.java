package QuadraticEquation;

public class ptb2 {
    private double a ,b ,c;
    private ptb2(){
    }
    public ptb2(double a , double b , double c){
        this.a = a;
        this.b = b;
        this.c = c;
    }
    public double getA(){
        return a;
    }
    public void setA(){
        this.a = a;
    }

    public double getB(){
        return b;
    }

    public void setB(){
        this.b = b;
    }
    public double getC(){
        return c;
    }
    public void setC(){
        this.c = c;
    }

}
